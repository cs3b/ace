# frozen_string_literal: true

require "test_helper"

class SynthesisIntegrationTest < AceReviewTest
  def setup
    super
    @session_dir = File.join(@test_dir, "session")
    FileUtils.mkdir_p(@session_dir)

    # Reset cached config before each test
    Ace::Review.reset_config!

    # Create test config with synthesis enabled
    create_test_config(<<~YAML)
      defaults:
        model: "test-model"
        preset: "test"
      synthesis:
        enabled: true
        default_model: "google:gemini-2.5-flash"
      presets:
        test:
          description: "Test preset"
          instructions:
            context:
              presets: ["project"]
          subject:
            context:
              sections:
                code:
                  title: "Code Changes"
                  files: ["test.rb"]
    YAML
  end

  def test_standalone_synthesis_command_execution
    # Create test reports
    report1 = File.join(@session_dir, "review-gemini-2-5-flash.md")
    report2 = File.join(@session_dir, "review-gpt-4.md")

    File.write(report1, sample_review_content("Gemini 2.5 Flash"))
    File.write(report2, sample_review_content("GPT-4"))

    # Mock LLM executor
    mock_llm_synthesis

    # Execute synthesis
    synthesizer = Ace::Review::Molecules::ReportSynthesizer.new
    result = synthesizer.synthesize(
      report_paths: [report1, report2],
      session_dir: @session_dir
    )

    # Verify result
    assert result[:success], "Synthesis should succeed"
    assert result[:output_file], "Should have output file"
    assert_equal File.join(@session_dir, "synthesis-report.md"), result[:output_file]

    # Verify file exists
    assert File.exist?(result[:output_file]), "Synthesis file should exist"

    # Verify content
    content = File.read(result[:output_file])
    assert_includes content, "Multi-Model Review Synthesis"
    assert_includes content, "gemini-2-5-flash, gpt-4"
  end

  def test_auto_synthesis_after_multi_model_execution
    # Create mock review output files that would be generated by multi-model executor
    model1_file = File.join(@session_dir, "review-gemini-2-5-flash.md")
    model2_file = File.join(@session_dir, "review-gpt-4.md")
    synthesis_file = File.join(@session_dir, "synthesis-report.md")

    File.write(model1_file, sample_review_content("Gemini 2.5 Flash"))
    File.write(model2_file, sample_review_content("GPT-4"))

    # Create manager with test config
    manager = Ace::Review::Organisms::ReviewManager.new(project_root: @test_dir)

    # Create options with multiple models and auto_execute enabled
    options = Ace::Review::Models::ReviewOptions.new(
      preset: "test",
      models: ["google:gemini-2.5-flash", "openai:gpt-4"],
      auto_execute: true
    )

    # Mock multi-model execution result
    multi_model_result = {
      success: true,
      results: {
        "google:gemini-2.5-flash" => {
          success: true,
          output_file: model1_file,
          model_slug: "gemini-2-5-flash"
        },
        "openai:gpt-4" => {
          success: true,
          output_file: model2_file,
          model_slug: "gpt-4"
        }
      },
      summary: {
        total_models: 2,
        success_count: 2,
        failure_count: 0
      }
    }

    # Track if synthesis was called
    synthesis_called = false
    synthesis_model_used = nil

    # Pre-generate mock response content (must be outside lambda)
    synthesis_content = mock_synthesis_response

    # Stub ReportSynthesizer to track calls and return mock result
    synthesizer_stub = ->(report_paths:, model: nil, session_dir: nil) {
      synthesis_called = true
      synthesis_model_used = model
      # Write synthesis output file
      File.write(synthesis_file, synthesis_content)
      {
        success: true,
        output_file: synthesis_file,
        models_synthesized: ["gemini-2-5-flash", "gpt-4"]
      }
    }

    # Stub MultiModelExecutor to return our mock result
    executor_stub = ->(**_args) { multi_model_result }

    # Create mock executor and synthesizer instances
    mock_executor = Ace::Review::Molecules::MultiModelExecutor.new
    mock_executor.define_singleton_method(:execute, executor_stub)

    mock_synthesizer = Ace::Review::Molecules::ReportSynthesizer.new
    mock_synthesizer.define_singleton_method(:synthesize, synthesizer_stub)

    # Test the internal methods directly
    # First verify should_synthesize? returns true for our setup
    assert manager.send(:should_synthesize?, multi_model_result, options),
           "should_synthesize? should return true for 2 successful reviews"

    # Now test auto_synthesize directly
    # We need to stub the synthesizer creation
    Ace::Review::Molecules::ReportSynthesizer.stub :new, mock_synthesizer do
      synthesis_result = manager.send(
        :auto_synthesize,
        multi_model_result,
        @session_dir,
        { preset: "test" },
        options
      )

      # Verify synthesis was called
      assert synthesis_called, "Synthesis should have been triggered"

      # Verify synthesis result
      assert synthesis_result[:success], "Synthesis should succeed"
      assert_equal synthesis_file, synthesis_result[:output_file]

      # Verify synthesis file was created
      assert File.exist?(synthesis_file), "Synthesis file should exist"

      # Verify synthesis content
      content = File.read(synthesis_file)
      assert_includes content, "Multi-Model Review Synthesis"
    end
  end

  def test_synthesis_skipped_when_disabled_via_flag
    # Create mock reviews
    report1 = File.join(@session_dir, "review-gemini.md")
    report2 = File.join(@session_dir, "review-gpt-4.md")

    File.write(report1, sample_review_content("Gemini"))
    File.write(report2, sample_review_content("GPT-4"))

    # Simulate ReviewManager synthesis check
    manager = Ace::Review::Organisms::ReviewManager.new(project_root: @test_dir)

    # Mock options with no_synthesize flag
    options = Ace::Review::Models::ReviewOptions.new(no_synthesize: true)

    # Mock result
    result = {
      success: true,
      results: {
        "gemini" => { success: true, output_file: report1 },
        "gpt-4" => { success: true, output_file: report2 }
      }
    }

    # should_synthesize? should return false
    should_run = manager.send(:should_synthesize?, result, options)
    refute should_run, "Synthesis should be skipped when no_synthesize flag is set"
  end

