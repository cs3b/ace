  def test_synthesis_skipped_with_less_than_two_successful_reviews
    # Create mock reviews (only one succeeds)
    report1 = File.join(@session_dir, "review-gemini.md")
    File.write(report1, sample_review_content("Gemini"))

    manager = Ace::Review::Organisms::ReviewManager.new(project_root: @test_dir)

    # Mock result with only one success
    result = {
      success: true,
      results: {
        "gemini" => { success: true, output_file: report1 },
        "gpt-4" => { success: false, error: "Model error" }
      }
    }

    # should_synthesize? should return false
    should_run = manager.send(:should_synthesize?, result, nil)
    refute should_run, "Synthesis should be skipped with less than 2 successful reviews"
  end

  def test_synthesis_uses_custom_model
    # Create test reports
    report1 = File.join(@session_dir, "review-gemini.md")
    report2 = File.join(@session_dir, "review-gpt-4.md")

    File.write(report1, sample_review_content("Gemini"))
    File.write(report2, sample_review_content("GPT-4"))

    # Track which model was used for synthesis
    synthesis_model_used = nil

    # Create synthesizer with stubbed executor
    synthesizer = Ace::Review::Molecules::ReportSynthesizer.new
    synthesizer.llm_executor.define_singleton_method(:execute) do |**args|
      synthesis_model_used = args[:model]
      {
        success: true,
        response: mock_synthesis_response,
        metadata: {}
      }
    end

    # Execute with custom synthesis model
    synthesizer.synthesize(
      report_paths: [report1, report2],
      model: "anthropic:claude-sonnet-4",
      session_dir: @session_dir
    )

    # Verify custom model was used
    assert_equal "anthropic:claude-sonnet-4", synthesis_model_used
  end

  private

  def sample_review_content(model_name)
    <<~MARKDOWN
      # Code Review by #{model_name}

      ## Summary
      Reviewed code changes for potential issues.

      ## Critical Issues
      1. Missing error handling in line 45
      2. Potential null pointer exception in line 67

      ## Recommendations
      - Add input validation
      - Improve test coverage
      - Document complex algorithms

      ## Positive Aspects
      - Good code structure
      - Clear variable naming
    MARKDOWN
  end

  def mock_synthesis_response
    <<~MARKDOWN
      # Multi-Model Review Synthesis

      ## Overview
      - Models: gemini, gpt-4

      ## Consensus Findings (All Models Agree)
      1. Missing error handling in line 45
      2. Need better test coverage

      ## Strong Recommendations (2+ Models)
      1. Add input validation
      2. Document complex algorithms

      ## Unique Insights
      ### From gemini:
      - Performance optimization opportunity in loop

      ### From gpt-4:
      - Consider using Result pattern for error handling

      ## Conflicting Views & Resolution
      No significant conflicts identified

      ## Prioritized Action Items
      1. Fix missing error handling (high priority - consensus)
      2. Add input validation (medium priority)
      3. Improve test coverage (medium priority)
      4. Document complex algorithms (low priority)
    MARKDOWN
  end

  def mock_llm_synthesis
    # Stub LLM executor globally for all ReportSynthesizer instances
    Ace::Review::Molecules::LlmExecutor.class_eval do
      alias_method :original_execute, :execute

      def execute(**_args)
        {
          success: true,
          response: mock_synthesis_response_global,
          metadata: { tokens: 1000 }
        }
      end

      def mock_synthesis_response_global
        <<~MARKDOWN
          # Multi-Model Review Synthesis

          ## Overview
          - Models: gemini-2-5-flash, gpt-4

          ## Consensus Findings (All Models Agree)
          1. Missing error handling

          ## Prioritized Action Items
          1. Fix error handling
        MARKDOWN
      end
    end
  end
